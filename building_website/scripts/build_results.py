#!/usr/bin/env python3
"""build_results.py — emit building_website/results.tex.

For every solved row in the website manifest, look up its
`time_needed_to_solve` (seconds) from the chapter's `data.json` and emit
one section-keyed tabular with row label + solve duration + section
total.
"""

from __future__ import annotations

import json
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[2]
DATA_DIR = REPO_ROOT / "building_website" / "website" / "data"
LEAN_ROOT = REPO_ROOT / "leanification"
OUT_PATH = REPO_ROOT / "building_website" / "results.tex"

PREAMBLE = r"""% results.tex — generated by building_website/scripts/build_results.py.
% Per-row solve durations sourced from each chapter's `data.json`
% (the `time_needed_to_solve` field, in seconds).
%
% Preamble (documentclass, packages) is supplied by the wrapping document;
% this file only emits body content.

\providecommand{\resultsSize}{\small}
"""


def fmt_seconds(s: int) -> str:
    s = int(s or 0)
    h, rem = divmod(s, 3600)
    m, sec = divmod(rem, 60)
    if h:
        return f"{h}h {m}m {sec}s"
    return f"{m}m {sec}s"


def find_chapter_data(chapter_num: int) -> Path | None:
    for cand in sorted(LEAN_ROOT.glob(f"Chapter{chapter_num}_*/data.json")):
        return cand
    return None


def section_table(rows_with_timing: list[tuple[str, int]]) -> list[str]:
    lines = [
        "\\noindent{\\resultsSize",
        "\\begin{tabular}{l r}",
        "\\hline",
        "\\textbf{Row} & \\textbf{Solve time} \\\\",
        "\\hline",
    ]
    total = 0
    for label, seconds in rows_with_timing:
        total += seconds
        lines.append(f"{label} & {fmt_seconds(seconds)} \\\\")
    lines.append("\\hline")
    lines.append(
        f"\\textbf{{Section total}} & \\textbf{{{fmt_seconds(total)}}} \\\\"
    )
    lines.append("\\hline")
    lines.append("\\end{tabular}\\par}")
    return lines


def main() -> None:
    manifest = json.loads((DATA_DIR / "manifest.json").read_text())
    out = [PREAMBLE]

    for chapter in manifest["chapters"]:
        data_path = find_chapter_data(chapter["chapter"])
        if data_path is None:
            continue
        chap_data = json.loads(data_path.read_text())
        by_ref = {r["ref"]: r for r in chap_data["rows"]}

        for section in chapter["sections"]:
            rows_with_timing: list[tuple[str, int]] = []
            for row in section["rows"]:
                if not row.get("available", False):
                    continue
                src = by_ref.get(row["ref"])
                if not src or src.get("solved") != "yes":
                    continue
                rows_with_timing.append(
                    (row["label"], src.get("time_needed_to_solve", 0) or 0)
                )
            if not rows_with_timing:
                continue
            out.append("")
            out.append(f"\\section*{{Section {section['section']}}}")
            out.extend(section_table(rows_with_timing))

    OUT_PATH.write_text("\n".join(out).rstrip() + "\n")
    print(f"Wrote {OUT_PATH} ({OUT_PATH.stat().st_size} bytes)")


if __name__ == "__main__":
    main()
