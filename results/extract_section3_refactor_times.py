#!/usr/bin/env python3
"""Build a per-row x per-iteration time table for Chapter 3.

By default it covers Sections 3.1, 3.2 and 3.3 (pass other sections on
the command line, e.g. `python3 extract_section3_refactor_times.py 3.1
3.2`).

Each ROW of the output is a def/claim in one of the chosen sections,
listed in lecture-notes order. Each COLUMN is an iteration:

    iteration 0 : initial solve (before any refactor)
    iteration k : the k-th refactor (chronological by `created_at`)

Each CELL is `time_needed_to_solve` (seconds) for that row in that
iteration; blank if the row was not (re-)solved in that iteration.

------------------------------------------------------------------
How the numbers are sourced (verified 2026-06-20)
------------------------------------------------------------------
`time_needed_to_solve` semantics:

  * A refactor's `refactor_data.json` row holds a FRESH re-solve time:
    `extras/initialize_refactor.py` resets it to 0 at refactor start, so
    the value is the wall-clock spent re-solving that row DURING that
    refactor.
  * `extras/apply_refactor_cleanup.py` (Phase 7d) OVERWRITES the main
    `data.json` row's `time_needed_to_solve` with the refactor value on a
    successful sync, and archives the previous main-data value in that
    refactor's `pre_refactor_rows.json` -> `pre_sync_row`. Hence a
    refactor's `pre_sync` value = the result of the PREVIOUS iteration
    for that row.

This chaining was verified to be internally consistent across 3.1-3.3:
for every row touched by more than one sync-aware refactor, each later
refactor's `pre_sync` equals the earlier refactor's result.

Two facts specific to Chapter 3:

  * `Refactor_total_order_helper_DONE_2026-06-07` predates the
    cleanup-sync mechanism: it has NO `pre_refactor_rows.json` and its
    re-solve times (def_3_8=2561, claim_3_2=2927, def_3_9=2355) were
    never written back into the main `data.json`. So they appear only in
    this refactor's own column, never as anyone's `pre_sync`, and the
    "initial" values of those rows are recovered cleanly from the cdmg
    refactor's `pre_sync` (which equals the genuine first-solve time).
  * `Refactor_cdmg_typed_edges_BUILDFAIL_2026-06-19` (the Lean build
    failed, but its data cleanup WAS synced) touched essentially all of
    3.1-3.3; current main-data values equal its refactor_data values.

Per-row "initial" rule:
  * if the row was touched by any refactor -> the earliest available
    `pre_sync` for that row;
  * otherwise (never refactored) -> the current main `data.json` value.
"""

import csv
import json
import sys
from pathlib import Path

REPO = Path(__file__).resolve().parent.parent
CH3 = REPO / "leanification" / "Chapter3_GraphTheory"
DATA = CH3 / "data.json"


def load_json(p: Path):
    return json.loads(p.read_text(encoding="utf-8"))


def fmt_time(seconds) -> str:
    """Format seconds as e.g. '1h 23m 45s', '45m 12s', '30s'."""
    if seconds is None:
        return ""
    s = int(seconds)
    h, rem = divmod(s, 3600)
    m, sec = divmod(rem, 60)
    if h > 0:
        return f"{h}h {m}m {sec}s"
    if m > 0:
        return f"{m}m {sec}s"
    return f"{sec}s"


def _tex_escape(s: str) -> str:
    return s.replace("_", r"\_")


def write_latex(out_tex, sections, refactors, table, cols, labels,
                totals, row_total, grand_total) -> None:
    """Emit a thesis-ready LaTeX float (requires booktabs + graphicx).

    Iteration columns are abbreviated to Init., R1..R{n}; the legend
    below the table spells out which refactor each Rk is."""
    ncols = 3 + len(labels) + 1  # ref, section, initial, R1..Rn, total
    col_spec = "l c r " + ("r " * len(labels)) + "r"

    # header cells
    head = ["Ref", "Sec.", "Init."]
    head += [f"R{i}" for i in range(1, len(labels) + 1)]
    head += [r"\textbf{Total}"]

    body = []
    for row in table:
        cells = [r"\texttt{" + _tex_escape(row["ref"]) + "}", row["section"],
                 fmt_time(row.get("initial"))]
        for lab in labels:
            cells.append(fmt_time(row.get(lab)))
        cells.append(r"\textbf{" + fmt_time(row_total[row["ref"]]) + "}")
        body.append("    " + " & ".join(cells) + r" \\")

    total_cells = [r"\textbf{TOTAL}", "", r"\textbf{" + fmt_time(totals["initial"]) + "}"]
    for lab in labels:
        total_cells.append(r"\textbf{" + fmt_time(totals[lab]) + "}")
    total_cells.append(r"\textbf{" + fmt_time(grand_total) + "}")
    total_row = "    " + " & ".join(total_cells) + r" \\"

    legend = [r"\item \textbf{Init.}: initial solve (before any refactor)."]
    for i, rf in enumerate(refactors, start=1):
        legend.append(
            r"\item \textbf{R" + str(i) + r"}: \texttt{"
            + _tex_escape(rf["label"]) + r"} ("
            + rf["created_at"][:10] + ").")

    sec_list = ", ".join(sorted(sections))
    tex = []
    tex.append("% Auto-generated by results/extract_section3_refactor_times.py")
    tex.append("% Requires: \\usepackage{booktabs} and \\usepackage{graphicx}.")
    tex.append("% Cells are time_needed_to_solve formatted as Xh Ym Zs;")
    tex.append("% blank = the row was not (re-)solved in that iteration.")
    tex.append(r"\begin{table}[htbp]")
    tex.append(r"  \centering")
    tex.append(r"  \caption{Time needed to solve per row and per "
               r"refactor iteration, Sections " + sec_list + r". Iteration~0 "
               r"is the initial solve; later columns are refactors in "
               r"chronological order. The bottom-right cell is the grand "
               r"total.}")
    tex.append(r"  \label{tab:refactor-times-"
               + "-".join(sorted(sections)).replace(".", "_") + "}")
    tex.append(r"  \resizebox{\textwidth}{!}{%")
    tex.append(r"  \begin{tabular}{" + col_spec + "}")
    tex.append(r"    \toprule")
    tex.append("    " + " & ".join(head) + r" \\")
    tex.append(r"    \midrule")
    tex.extend(body)
    tex.append(r"    \midrule")
    tex.append(total_row)
    tex.append(r"    \bottomrule")
    tex.append(r"  \end{tabular}%")
    tex.append(r"  }")
    tex.append(r"  \par\vspace{0.5em}")
    tex.append(r"  \begin{flushleft}\footnotesize")
    tex.append(r"  \begin{itemize}\setlength{\itemsep}{0pt}")
    tex.extend("  " + l for l in legend)
    tex.append(r"  \end{itemize}")
    tex.append(r"  \end{flushleft}")
    tex.append(r"\end{table}")
    tex.append("")
    out_tex.write_text("\n".join(tex), encoding="utf-8")


def main(sections) -> None:
    main_data = load_json(DATA)
    rows = [r for r in main_data["rows"] if r.get("section") in sections]
    sec_refs = [r["ref"] for r in rows]
    section_of = {r["ref"]: r.get("section") for r in rows}
    current = {r["ref"]: r.get("time_needed_to_solve") for r in rows}

    # ---- discover refactor folders, ordered chronologically -----------
    refactors = []
    for folder in sorted(CH3.glob("Refactor_*")):
        rd_path = folder / "refactor_data.json"
        if not rd_path.exists():
            continue
        rd = load_json(rd_path)
        label = rd.get("refactor_name") or folder.name
        result_map = {row["ref"]: row.get("time_needed_to_solve")
                      for row in rd.get("rows", [])}
        pre_path = folder / "pre_refactor_rows.json"
        pre_map = {}
        if pre_path.exists():
            for entry in load_json(pre_path):
                pre_map[entry.get("ref")] = \
                    entry.get("pre_sync_row", {}).get("time_needed_to_solve")
        refactors.append({
            "created_at": rd.get("created_at", ""),
            "folder": folder.name,
            "label": label,
            "results": result_map,
            "pre_sync": pre_map,
        })
    refactors.sort(key=lambda r: r["created_at"])

    # ---- initial (iteration 0) time per row ---------------------------
    initial = {}
    for ref in sec_refs:
        val = None
        for rf in refactors:  # chronological
            if rf["pre_sync"].get(ref) is not None:
                val = rf["pre_sync"][ref]
                break
        if val is None:
            # never refactored -> the current value IS the first solve
            val = current[ref]
        initial[ref] = val

    # ---- assemble table -----------------------------------------------
    labels = [rf["label"] for rf in refactors]
    table = []
    for ref in sec_refs:
        row = {"ref": ref, "section": section_of[ref],
               "initial": initial.get(ref)}
        for rf in refactors:
            row[rf["label"]] = rf["results"].get(ref)
        table.append(row)

    secs_tag = "_".join(s.replace(".", "_") for s in sorted(sections))
    out_csv = REPO / "results" / f"section{secs_tag}_refactor_times.csv"
    out_md = REPO / "results" / f"section{secs_tag}_refactor_times.md"
    out_tex = REPO / "results" / f"section{secs_tag}_refactor_times.tex"

    cols = ["initial"] + labels

    # ---- column totals (per iteration) and per-row totals -------------
    # blanks/None ignored throughout.
    totals = {c: sum(row.get(c) for row in table if row.get(c) is not None)
              for c in cols}
    row_total = {row["ref"]: sum(row.get(c) for c in cols
                                 if row.get(c) is not None)
                 for row in table}
    grand_total = sum(totals[c] for c in cols)  # = sum of every cell

    # ---- CSV (raw seconds for machine use) ------------------------------
    with out_csv.open("w", newline="", encoding="utf-8") as f:
        w = csv.writer(f)
        w.writerow(["ref", "section"] + cols + ["row_total"])
        for row in table:
            w.writerow([row["ref"], row["section"]]
                       + [("" if row.get(c) is None else row.get(c))
                          for c in cols]
                       + [row_total[row["ref"]]])
        w.writerow(["TOTAL", ""] + [totals[c] for c in cols] + [grand_total])

    # ---- Markdown -----------------------------------------------------
    hdr = ["ref", "section", "iter 0<br>initial"]
    for i, rf in enumerate(refactors, start=1):
        hdr.append(f"iter {i}<br>{rf['label']}<br>{rf['created_at'][:10]}")
    hdr.append("**row total**")

    lines = [
        f"# Sections {', '.join(sorted(sections))} — "
        "time needed to solve, per refactor iteration",
        "",
        "Cell = `time_needed_to_solve` formatted as Xh Ym Zs for that row "
        "in that iteration. Blank = the row was not (re-)solved in that "
        "iteration. Iteration 0 is the initial solve; later columns are "
        "refactors in chronological order. See "
        "`extract_section3_refactor_times.py` for exactly how each number "
        "is sourced.",
        "",
        "| " + " | ".join(hdr) + " |",
        "|" + "|".join(["---"] * len(hdr)) + "|",
    ]
    for row in table:
        cells = [row["ref"], row["section"], fmt_time(row.get("initial"))]
        for rf in refactors:
            cells.append(fmt_time(row.get(rf["label"])))
        cells.append(f"**{fmt_time(row_total[row['ref']])}**")
        lines.append("| " + " | ".join(cells) + " |")

    # totals row (per-iteration totals; bottom-right = grand total)
    tcells = ["**TOTAL**", ""]
    for c in cols:
        tcells.append(f"**{fmt_time(totals[c])}**")
    tcells.append(f"**{fmt_time(grand_total)}**")
    lines.append("| " + " | ".join(tcells) + " |")

    lines += ["", "## Refactor iterations (chronological)", "",
              "| iter | refactor | created_at | folder |",
              "|---|---|---|---|",
              "| 0 | (initial solve) | — | — |"]
    for i, rf in enumerate(refactors, start=1):
        lines.append(f"| {i} | {rf['label']} | {rf['created_at']} | "
                     f"{rf['folder']} |")
    lines.append("")

    out_md.write_text("\n".join(lines), encoding="utf-8")

    # ---- LaTeX --------------------------------------------------------
    write_latex(out_tex, sections, refactors, table, cols, labels,
                totals, row_total, grand_total)

    print("\n".join(lines))
    print(f"\nWrote: {out_csv}")
    print(f"Wrote: {out_md}")
    print(f"Wrote: {out_tex}")


if __name__ == "__main__":
    secs = set(sys.argv[1:]) or {"3.1", "3.2", "3.3"}
    main(secs)
